package eg2;

public class Calculator {

	public int add(int a,int b) {
		return a+b;
	}
	
	public static int multiply(int a,int b) {
		return a*b;
	}
	
	public int add(int a,int b,int c) {
		return a+b+c;
	}
	
	public float add(float a,float b) {
		return a+b;
	}
	
	public String add(String a,String b) {
		return a+b;
	}
}
